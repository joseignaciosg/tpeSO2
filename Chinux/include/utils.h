#ifndef _utils_
#define _utils_

/********************************************************************
 *str_len
 *Returns the size of a given string
 ********************************************************************/
int str_len(char *s);

/*******************************************************************
 *  reverse:
 *
 *  Reverses string s in place
 *******************************************************************/
void reverse(char s[]);

/*******************************************************************
 * strcmp
 *
 * Compares two strings
 *******************************************************************/
int strcmp(char * s1, char * s2);

/*******************************************************************
 * strcopy
 *
 * Copies two strings
 *******************************************************************/
void strcopy(char * copy, char * s, int size);

/********************************************************************
 * itoa
 *
 * Converts an integer to string
 *********************************************************************/
int itoa(int n);

/*******************************************************************
 * ltoa
 *
 * Converts a long to string
 *********************************************************************/
int ltoa(unsigned long int n);

/********************************************************************
 * ftoa
 *
 * Converts a double to string
 *********************************************************************/
int ftoa(double n);

void memcpy(char* a, char* b, int len);

#endif
